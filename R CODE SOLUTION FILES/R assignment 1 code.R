# Arithmetic operations
a <- 10
b <- 5

# Addition
addition <- a + b
print(paste("Addition:", addition))

# Subtraction
subtraction <- a - b
print(paste("Subtraction:", subtraction))

# Multiplication
multiplication <- a * b
print(paste("Multiplication:", multiplication))

# Division
division <- a / b
print(paste("Division:", division))

# Exponentiation
exponentiation <- a ^ b
print(paste("Exponentiation:", exponentiation))

